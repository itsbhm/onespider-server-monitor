-- STEP 5: AUTO-CLEANUP FUNCTION (Optional)
-- ═══════════════════════════════════════════════
-- Deletes logs older than 1 year to keep DB size in free tier
-- You can call this manually or set up a Supabase cron

CREATE OR REPLACE FUNCTION cleanup_old_logs()
RETURNS void AS $$
BEGIN
    DELETE FROM uptime_logs 
    WHERE created_at < NOW() - INTERVAL '1 year';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

COMMENT ON FUNCTION cleanup_old_logs IS 'Removes logs older than 1 year to stay within Supabase free tier limits';


