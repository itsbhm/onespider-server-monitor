-- ═══════════════════════════════════════════════
-- STEP 1: MAIN TABLE
-- ═══════════════════════════════════════════════

-- Drop if exists (safe for re-runs)
DROP VIEW IF EXISTS quarterly_uptime_report CASCADE;
DROP VIEW IF EXISTS monthly_uptime_report CASCADE;
DROP VIEW IF EXISTS weekly_uptime_report CASCADE;
DROP VIEW IF EXISTS daily_uptime_report CASCADE;
DROP TABLE IF EXISTS uptime_logs CASCADE;

-- Core table: stores every health check result
CREATE TABLE uptime_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at  TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    icmp_up     BOOLEAN NOT NULL,
    http_up     BOOLEAN NOT NULL,
    latency_ms  FLOAT DEFAULT 0,
    error_message TEXT
);

-- Add comment for documentation
COMMENT ON TABLE uptime_logs IS 'Stores infrastructure health check results every 5 minutes';
COMMENT ON COLUMN uptime_logs.icmp_up IS 'Whether ICMP ping was successful';
COMMENT ON COLUMN uptime_logs.http_up IS 'Whether HTTP request was successful';
COMMENT ON COLUMN uptime_logs.latency_ms IS 'Average ICMP ping latency in milliseconds';
COMMENT ON COLUMN uptime_logs.error_message IS 'Error details when check fails';


-- ═══════════════════════════════════════════════
-- STEP 2: INDEXES FOR PERFORMANCE
-- ═══════════════════════════════════════════════

-- Fast lookup: latest logs first
CREATE INDEX idx_logs_created_desc ON uptime_logs (created_at DESC);

-- Fast lookup: find downtime incidents
CREATE INDEX idx_logs_downtime ON uptime_logs (created_at DESC) 
    WHERE icmp_up = FALSE AND http_up = FALSE;


-- ═══════════════════════════════════════════════
-- STEP 3: ROW LEVEL SECURITY (RLS) & PUBLIC ACCESS
-- ═══════════════════════════════════════════════

-- Enable RLS (Supabase best practice)
ALTER TABLE uptime_logs ENABLE ROW LEVEL SECURITY;

-- 1. Allow service_role full access (for Node.js script insertions)
CREATE POLICY "Allow service role full access" ON uptime_logs
    FOR ALL USING (true) WITH CHECK (true);

-- 2. Allow PUBLIC (anon) read-only access (for public Status Page dashboard)
CREATE POLICY "Allow public read access" ON uptime_logs
    FOR SELECT USING (true);

-- 3. Grant table permissions to anon role
GRANT SELECT ON uptime_logs TO anon;


-- ═══════════════════════════════════════════════
-- STEP 4: SLA REPORT VIEWS (All IST-Aligned)
-- ═══════════════════════════════════════════════

-- ─── Daily Report ───────────────────────────────
-- Groups checks by IST day
CREATE OR REPLACE VIEW daily_uptime_report AS
SELECT 
    date_trunc('day', created_at AT TIME ZONE 'Asia/Kolkata') AS report_date,
    COUNT(*) AS total_checks,
    ROUND(
        (SUM(CASE WHEN icmp_up = TRUE OR http_up = TRUE THEN 1 ELSE 0 END)::numeric 
         / COUNT(*)::numeric) * 100, 2
    ) AS uptime_percentage,
    ROUND(AVG(latency_ms) FILTER (WHERE icmp_up = TRUE)::numeric, 2) AS avg_latency_ms,
    ROUND(MIN(latency_ms) FILTER (WHERE icmp_up = TRUE)::numeric, 2) AS min_latency_ms,
    ROUND(MAX(latency_ms) FILTER (WHERE icmp_up = TRUE)::numeric, 2) AS max_latency_ms,
    SUM(CASE WHEN NOT icmp_up AND NOT http_up THEN 1 ELSE 0 END) AS total_downtime_checks,
    SUM(CASE WHEN icmp_up = TRUE THEN 1 ELSE 0 END) AS icmp_success_count,
    SUM(CASE WHEN http_up = TRUE THEN 1 ELSE 0 END) AS http_success_count
FROM uptime_logs
GROUP BY 1 ORDER BY 1 DESC;


